DECLARE @questionId INT,
		@optionId INT;

SELECT @questionId = q.Id
FROM dbo.Questions q
JOIN dbo.QuestionOptions qo
	ON q.Id = qo.QuestionId
	AND q.Number = '75'
JOIN dbo.Surveys s
	ON q.SurveyId = s.Id
	AND s.Name = 'HEARING'
JOIN dbo.Options o
	ON qo.OptionId = o.Id;

SELECT @optionId = o.Id
FROM dbo.Options o
WHERE o.Value = 'Both Deaf and Hard of Hearing';

INSERT INTO dbo.QuestionOptions(QuestionId, OptionId, [Weight]) VALUES (@questionId, @optionId, 0);

